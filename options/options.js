async function loadLicense() {
  const { slumber_license } = await chrome.storage.sync.get('slumber_license');
  renderLicenseState(slumber_license ?? null);
}

function renderLicenseState(license) {
  const active = Boolean(license?.valid);
  $('pro-active-card').hidden = !active;
  $('free-card').hidden       =  active;
  if (active) {
    $('pro-txn-id').textContent    = license.licenseKey ?? '—';
    $('pro-license-email').textContent = license.email ? `Registered to ${license.email}` : '';
  }
}

async function validateLicenseKey(key) {
  const res = await fetch(
    `${VALIDATE_ENDPOINT}?key=${encodeURIComponent(key)}`,
    { signal: AbortSignal.timeout(10_000) },
  );
  if (!res.ok) throw Object.assign(new Error(`HTTP ${res.status}`), { status: res.status });
  return res.json(); // { valid: bool, email: string }
}

async function activateLicense(key) {
  const btn    = $('btn-activate-key');
  const input  = $('license-key-input');

  btn.disabled   = true;
  btn.textContent = 'Checking…';
  input.disabled  = true;
  setLicenseStatus('');

  let data;
  try {
    data = await validateLicenseKey(key);
  } catch (err) {
    btn.disabled    = false;
    btn.textContent = 'Activate';
    input.disabled  = false;

    // Network failure — keep whatever state is already stored
    const { slumber_license } = await chrome.storage.sync.get('slumber_license');
    if (slumber_license?.valid) {
      setLicenseStatus('Could not reach the server. Your existing activation has been kept.', 'warn');
    } else {
      setLicenseStatus('Could not reach the validation server. Check your connection and try again.', 'error');
    }
    return;
  }

  btn.disabled    = false;
  btn.textContent = 'Activate';
  input.disabled  = false;

  if (!data.valid) {
    setLicenseStatus('This key was not recognised. Check your Paddle receipt and try again.', 'error');
    await chrome.storage.sync.remove('slumber_license');
    renderLicenseState(null);
    return;
  }

  const license = {
    licenseKey:  key,
    valid:       true,
    email:       data.email ?? '',
    validatedAt: Date.now(),
  };
  await chrome.storage.sync.set({ slumber_license: license });
  renderLicenseState(license);
  setLicenseStatus('License activated — Slumber Pro is now active.', 'success');
}

async function deactivateLicense() {
  if (!confirm('Remove the Pro license from this Chrome profile? You can re-enter your key later.')) return;
  await chrome.storage.sync.remove('slumber_license');
  renderLicenseState(null);
}

function setLicenseStatus(message, type = '') {
  const el = $('license-key-status');
  if (!el) return;
  el.textContent = message;
  el.className   = 'license-key-status' + (type ? ` is-${type}` : '');
  el.hidden      = !message;
}

function bindLicenseEvents() {
  $('btn-upgrade').addEventListener('click', () => {
    chrome.tabs.create({ url: PADDLE_PAYMENT_LINK });
  });

  $('link-login').addEventListener('click', e => {
    e.preventDefault();
    $('license-key-wrap').hidden = false;
    $('license-key-input').focus();
  });

  $('btn-activate-key').addEventListener('click', async () => {
    const raw = $('license-key-input').value.trim();
    if (!LICENSE_KEY_RE.test(raw)) {
      setLicenseStatus('Invalid format — key should look like XXXX-XXXX-XXXX-XXXX.', 'error');
      return;
    }
    await activateLicense(raw);
  });

  $('license-key-input').addEventListener('keydown', e => {
    if (e.key === 'Enter') $('btn-activate-key').click();
  });

  $('license-key-input').addEventListener('input', () => setLicenseStatus(''));

  $('link-deactivate').addEventListener('click', e => {
    e.preventDefault();
    deactivateLicense();
  });
}

