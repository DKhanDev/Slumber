const $ = id => document.getElementById(id);

function msg(type, payload = {}) {
  return chrome.runtime.sendMessage({ type, payload });
}

function showSaveConfirm(id) {
  const el = $(id);
  if (!el) return;
  el.hidden = false;
  clearTimeout(el._timer);
  el._timer = setTimeout(() => { el.hidden = true; }, 2200);
}

