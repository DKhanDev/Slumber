const PADDLE_PAYMENT_LINK = 'https://dkhan.dev/checkout?product=slumber-pro';
const VALIDATE_ENDPOINT   = 'https://dkhandev-site-payment-worker.wolf001349.workers.dev/validate';

// Matches keys generated by the Cloudflare Worker: XXXX-XXXX-XXXX-XXXX
const LICENSE_KEY_RE = /^[A-Z0-9]{4}-[A-Z0-9]{4}-[A-Z0-9]{4}-[A-Z0-9]{4}$/i;

// ---------------------------------------------------------------------------
// Helpers
