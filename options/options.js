async function init() {
  const manifest = chrome.runtime.getManifest();
  const verEl = $('version-label');
  if (verEl) verEl.textContent = `v${manifest.version}`;

  // Wire GitHub sidebar link
  const ghLink = $('link-github');
  if (ghLink) ghLink.href = GITHUB_URL;

  const [settingsRes, { slumber_license }] = await Promise.all([
    msg('GET_SETTINGS'),
    chrome.storage.sync.get('slumber_license'),
  ]);
  if (settingsRes.ok) {
    applySettings(settingsRes.settings, Boolean(slumber_license?.valid));
    _whitelist = settingsRes.settings.whitelist ?? [];
  }

  bindGeneralEvents();
  bindShortcutsEvents();
  bindScheduleEvents();
  bindWhitelistEvents();
  bindLicenseEvents();
  bindStatsEvents();
  await maybeShowWelcomeCard();

  routeFromHash();
}

